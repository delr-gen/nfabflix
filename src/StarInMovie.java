public class StarInMovie {
    private final String starName;

    private final String filmId;

    public StarInMovie(String starName, String filmId) {
        this.starName = starName;
        this.filmId = filmId;
    }

    public String getStarName() {
        return starName;
    }

    public String getFilmId() {
        return filmId;
    }
}
